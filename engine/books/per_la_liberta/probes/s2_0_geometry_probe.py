"""S2.0 — bbox + text↔box-alignment PROBE (GATE, issue #18; ENGINE_STRUCTURE_TASKS S2.0).

Reproducible evidence for the geometry-pole gate. Revised after the 5-lens adversarial audit
(2026-06-29) — see `docs/probes/s2_0_adversarial_audit.md`. Measures, on real PLL pages:

  (i)  does a usable OCR word-box layer EXIST?
       Native `get_text` on both source PDFs is empty (image scans), so a box layer is GENERATED by
       a fresh PyMuPDF+Tesseract(ita) OCR pass — a different engine from every witness.

  (ii) can those boxes be TRUSTED as anchors for the witness text?
       The boxes come from a *different* OCR engine than every witness (copy1/2 = IA-Tesseract,
       copy3 = Gemini), so "an atom carries its primary witness's box" is a cross-engine match.
       copy3 (Gemini) is the only witness with a per-page char map, so it is the alignment target;
       copy1 was independently confirmed column-ordered (audit), so copy3 is representative.

Coverage numbers reported per page (token = whitespace split, lowercased, edge-punct stripped):
  - BoW              : multiset overlap, order-independent  -> token-anchorability ceiling
  - content BoW      : BoW over distinctive tokens only (function words dropped) -> what re-bind gets
  - naive ordered    : LCS coverage in raw OCR (full-width) reading order
  - col-aware ordered: LCS coverage after recovering column reading order from the box COORDINATES

Reading order on PLL's two-column body collapses naive ordered to ~0.50 (full-width OCR interleaves
the columns); recovering column order from the box coordinates lifts it back toward the BoW ceiling.

COLUMN DETECTION HERE is a projection-profile gutter test (contiguous central low-density run, near
page-center, splitting the page into two substantially-populated halves) — a real improvement over the
audit-killed `min(single-bin)` detector (which flagged EVERY page two-column), but still a PROBE
heuristic, not the production matcher. It has known residual edge errors (a few sparse back-matter /
footnote pages); those are reported, not hidden. A robust single-vs-N-column detector is S2.1 work.

Reading order is NOT, in general, the geometry's job: when a column-correct text witness exists (PLL),
it is the reading-order oracle and boxes position-match it; the geometric detector is a cross-check.
When no prior witness exists (image-only source), the geometric detector is the primary order source,
backstopped by human-in-the-loop review of low-confidence pages.

Run (Tesseract tessdata must be discoverable):
    cd engine && TESSDATA_PREFIX=/opt/homebrew/share/tessdata \
      uv run python books/per_la_liberta/probes/s2_0_geometry_probe.py [out.json]
Override inputs via env: PLL_LOC_PDF (default repo-root LOC scan), PLL_OCR_DPI (default 300).
"""
