def tokens(text: str) -> list[str]:
    return [n for n in (_EDGE.sub("", t).lower() for t in text.split()) if n]


def content_tokens(toks: list[str]) -> list[str]:
    return [t for t in toks if t not in _STOP and len(t) > 2 and not t.isdigit()]


def ordered_coverage(wit: list[str], seq: list[str]) -> float:
    if not wit:
        return float("nan")
    sm = SequenceMatcher(a=wit, b=seq, autojunk=False)
    return sum(n for _, _, n in sm.get_matching_blocks()) / len(wit)


def bow_coverage(wit: list[str], seq: list[str]) -> float:
    if not wit:
        return float("nan")
    oc = Counter(seq)
    wc = Counter(wit)
    return sum(min(wc[w], oc[w]) for w in wc) / len(wit)


def detect_columns(words, width: float) -> tuple[int, float]:
    """(n_cols, split_x) via a projection-profile gutter test.

    Two-column iff a CONTIGUOUS run of central low-density bins (a real gutter), located NEAR the
    page center, splits the boxes into two SUBSTANTIALLY-populated halves. This rejects the audit's
    failure mode — a single stray empty bin in sparse single-column text reading as a "gutter".
    Still a probe heuristic with residual edge errors; a robust detector is S2.1.
    """
    if len(words) < 25:
        return (1, width)
    bins = [0] * 100
    for w in words:
        bins[max(0, min(99, int(((w[0] + w[2]) / 2) / width * 100)))] += 1   # clamp BOTH ends
    low = 0.15 * (max(bins) or 1)
    best_len = best_start = run = 0
    start = 33
    for b in range(33, 67):                          # central third only
        if bins[b] <= low:
            if run == 0:
                start = b
            run += 1
            if run > best_len:
                best_len, best_start = run, start
        else:
            run = 0
    if best_len < 3:                                 # need a real (>=3-bin) contiguous gutter
        return (1, width)
    split = (best_start + best_len / 2) / 100 * width
    if not (0.40 * width <= split <= 0.60 * width):  # gutter must be near center
        return (1, width)
    left = sum(1 for w in words if (w[0] + w[2]) / 2 < split) / len(words)
    if 0.28 <= left <= 0.72:                         # both columns genuinely populated
        return (2, split)
    return (1, width)


def reading_order(words, width: float) -> tuple[list[str], int]:
    """Recover reading order from box COORDINATES alone via detect_columns."""
    if not words:
        return ([], 1)
    n_cols, split = detect_columns(words, width)
    heights = [w[3] - w[1] for w in words]
    med = statistics.median(heights)
    line_h = (med if med > 0 else 10) * 0.6          # explicit guard (no falsy-zero `or`)

    def key(w):
        return (round(((w[1] + w[3]) / 2) / line_h), w[0])

    if n_cols == 2:
        left = sorted([w for w in words if (w[0] + w[2]) / 2 < split], key=key)
        right = sorted([w for w in words if (w[0] + w[2]) / 2 >= split], key=key)
        seq = left + right
    else:
        seq = sorted(words, key=key)
    return ([w[4] for w in seq], n_cols)


def box_smoke(words, rect) -> tuple[float, float]:
    """(boxes_in_page, vmono) — a SMOKE TEST that OCR ran, not an anchorability signal.

    OCR boxes are inside the page they were OCR'd from by construction, so boxes_in_page is ~1.0
    even for hallucinated noise boxes; it is reported only to confirm the geometry is well-formed.
    The real anchorability evidence is the witness coverage metrics.
    """
    if not words:
        return (1.0, 1.0)
    in_bounds = sum(
        1 for w in words
        if w[0] >= -1 and w[1] >= -1 and w[2] <= rect.width + 1 and w[3] <= rect.height + 1
        and w[2] > w[0] and w[3] > w[1]
    ) / len(words)
    tops = [w[1] for w in words]
    mono = sum(1 for a, b in zip(tops, tops[1:]) if b >= a - 2) / max(1, len(tops) - 1)
    return (in_bounds, mono)


def ocr_words(doc, scan: int):
    pg = doc[scan - 1]
    tp = pg.get_textpage_ocr(flags=0, language=OCR_LANG, dpi=DPI, full=True)
    return pg.get_text("words", textpage=tp), pg.rect


def measure(doc, scan: int, copy3: str, pm: dict):
    """All per-page metrics for one scan page."""
    words, rect = ocr_words(doc, scan)
    cs, ce = pm.get(scan, (None, None))
    wit = tokens(copy3[cs:ce]) if cs is not None else []
    ocr_tok = tokens(" ".join(w[4] for w in words))
    seq, n_cols = reading_order(words, rect.width)
    inb, vmono = box_smoke(words, rect)
    return {
        "scan": scan, "boxes": len(words), "wit_tokens": len(wit), "n_cols": n_cols,
        "bow": bow_coverage(wit, ocr_tok),
        "content_bow": bow_coverage(content_tokens(wit), content_tokens(ocr_tok)),
        "naive_ordered": ordered_coverage(wit, ocr_tok),
        "colaware_ordered": ordered_coverage(wit, tokens(" ".join(seq))),
        "boxes_in_page": inb, "vmono": vmono,
        "box_wit_ratio": len(words) / max(1, len(wit)),
    }


def _agg(rows: list[dict], field: str) -> dict:
    vals = [r[field] for r in rows if r[field] == r[field]]  # drop nan
    if not vals:
        return {"n": 0}
    return {
        "n": len(vals), "median": round(statistics.median(vals), 3),
        "mean": round(statistics.mean(vals), 3), "min": round(min(vals), 3),
        "pass@0.85": round(sum(v >= PRIMARY_BAR for v in vals) / len(vals), 3),
    }


def run():
    copy3 = COPY3.read_text(encoding="utf-8")
    pm = {e["page"]: (e["char_start"], e["char_end"]) for e in json.loads(PAGEMAP.read_text())}
    doc = fitz.open(PDF)
    out = {"pdf": PDF.name, "pages": doc.page_count, "dpi": DPI, "lang": OCR_LANG,
           "primary_bar": PRIMARY_BAR, "stratified": [], "sweep": []}

    print(f"# S2.0 geometry probe  pdf={PDF.name} pages={doc.page_count} dpi={DPI}\n")
    print("## stratified sample")
    hdr = f"{'scan':>4} {'stratum':<18} {'box':>4} {'wit':>4} {'cols':>4} {'BoW':>5} {'cBoW':>5} {'naive':>5} {'colaw':>5} {'b/w':>5}"
    print(hdr)
    for scan, stratum in STRATIFIED:
        t0 = time.time()
        r = measure(doc, scan, copy3, pm)
        r["stratum"] = stratum
        r["secs"] = round(time.time() - t0, 1)
        out["stratified"].append(r)
        f = lambda x: f"{x:5.2f}" if x == x else "  nan"  # noqa: E731
        print(f"{scan:>4} {stratum:<18} {r['boxes']:>4} {r['wit_tokens']:>4} {r['n_cols']:>4} "
              f"{f(r['bow'])} {f(r['content_bow'])} {f(r['naive_ordered'])} {f(r['colaware_ordered'])} "
              f"{r['box_wit_ratio']:5.1f}")

    print("\n## book-wide breadth sweep (every 7th page, wit>=20) — ordered coverage now computed")
    for scan in range(7, doc.page_count, 7):
        if scan not in pm:
            continue
        r = measure(doc, scan, copy3, pm)
        if r["wit_tokens"] < 20:
            continue
        out["sweep"].append(r)

    sweep = out["sweep"]
    two_col = [r for r in sweep if r["n_cols"] == 2]
    out["summary"] = {
        "sweep_n": len(sweep),
        "sweep_two_col_detected": len(two_col),
        "bow": _agg(sweep, "bow"),
        "content_bow": _agg(sweep, "content_bow"),
        "naive_ordered": _agg(sweep, "naive_ordered"),
        "colaware_ordered": _agg(sweep, "colaware_ordered"),
        "colaware_ordered_two_col_only": _agg(two_col, "colaware_ordered"),
    }
    s = out["summary"]
    print("\n## summary (breadth sweep is the honest basis — ordered coverage on n>=30)")
    print(f"sweep n={s['sweep_n']}  two-column detected={s['sweep_two_col_detected']}/{s['sweep_n']}")
    for k in ("bow", "content_bow", "naive_ordered", "colaware_ordered", "colaware_ordered_two_col_only"):
        a = s[k]
        print(f"  {k:<32} median={a.get('median')}  mean={a.get('mean')}  "
              f"min={a.get('min')}  pass@{PRIMARY_BAR}={a.get('pass@0.85')}  n={a.get('n')}")

    if len(sys.argv) > 1:
        Path(sys.argv[1]).write_text(json.dumps(out, indent=2))
        print(f"\nwrote {sys.argv[1]}")


if __name__ == "__main__":
    run()
